while c<(len(sort_numbers)):
    if sort_numbers[c]%2==0: #если остаток от деленя на 2 равен 0 то число чётное..
        a = a + 1 #увеличиваем количествов чётных на единицу
        even=even+numbers[c] #добавляем число к сумме чётных
    else: #..если остаток больше 0 то число нечётное
        b = b + 1#увеличиваем количествов нечётных на единицу
        odd=odd+numbers[c]#добавляем число к сумме нечётных
    c=c+1
